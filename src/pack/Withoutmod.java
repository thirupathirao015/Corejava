package pack;

public class Withoutmod {

	public static void main(String[] args) {
     int n=11;
     for(int i=1;i<=10;i++)
     {
    	if(2*i==n)
    	{
    		System.out.println("Given number is even:"+n);
    	}
    	if(2*i+1==n)
    	{
    		System.out.println("Given number is odd number:"+n);
    	}
     }
	}

}
